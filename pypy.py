def print_7seg(num):
    if num >= 1000:
        return
    elif num >= 100:
        digit = 3
        hundred = int(num / 100)
        ten = int(int(num % 100) / 10)
        one = int(int(num % 100) % 10)
        for line in range(9):
            print(numbers[hundred][line] + "    " + numbers[ten][line] + "    " + numbers[one][line])
    elif num >= 10:
        digit = 2
        ten = int(num / 10)
        one = int(num % 10)
        for line in range(9):
            print(numbers[ten][line] + "    " + numbers[one][line])
    else:
        digit = 1
        for line in range(9):
            print(numbers[num][line])
    print("                 " * digit + "m/s")


